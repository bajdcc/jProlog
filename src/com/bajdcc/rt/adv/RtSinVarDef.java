package com.bajdcc.rt.adv;

/**
 * 单变量
 */
public class RtSinVarDef {

    private final int id;
    private final int type;

    public RtSinVarDef(int id, int type) {
        this.id = id;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public int getType() {
        return type;
    }
}
